    /**
     * Returns a mapped array for using on Select widget
     *
     * @param boolean $check_status
     * @return array
     */
    public static function getSelectMap($check_status = false, $defaul_value = NULL)
    {
        $query = self::find()->where(['<>', 'code', 99]);
        if($check_status)
        {
            $query->andWhere(['status' => self::STATUS_ACTIVE]);
        }
        if (!is_null($defaul_value)){
            $query->andWhere(['code' => '01']);
        }

        $models = $query->asArray()->all();

        $array_map = [];

        if(count($models)>0)
        {
            foreach ($models AS $index => $model)
            {
                $array_map[$model['id']] = $model['code'].' - '.$model['name'];
            }
        }

        return $array_map;
    }

